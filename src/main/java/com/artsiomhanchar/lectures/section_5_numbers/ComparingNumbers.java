package com.artsiomhanchar.lectures.section_5_numbers;

public class ComparingNumbers {
    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 3;

        System.out.println(num1 == num2);
        System.out.println(num1 > num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 != num2);
    }
}
